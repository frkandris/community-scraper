# Stubs for languages not yet fully translated — fall back to English
for _code in LANGUAGES:
    if _code not in _T:
        _T[_code] = {}


def _detect_lang(request: Request) -> str:
    lang = request.cookies.get("lang", "")
    if lang in LANGUAGES:
        return lang
    accept = request.headers.get("accept-language", "")
    for part in accept.split(","):
        code = part.split(";")[0].strip().split("-")[0].lower()
        if code in LANGUAGES:
            return code
    return "en"


def make_t(lang: str):
    base = _T.get("en", {})
    overrides = _T.get(lang, {})
    merged = {**base, **overrides}

    def t(key: str, **kwargs) -> str:
        text = merged.get(key, key)
        if kwargs:
            try:
                text = text.format(**kwargs)
            except (KeyError, IndexError):
                pass
        return text
    return t


def lang_context(request: Request) -> dict:
    lang = _detect_lang(request)
    return {
        "lang": lang,
        "lang_dir": "rtl" if lang in RTL_LANGS else "ltr",
        "t": make_t(lang),
        "languages": dict(sorted(LANGUAGES.items(), key=lambda x: x[1]["name"])),
        "current_lang": LANGUAGES.get(lang, LANGUAGES["en"]),
        "topic_labels": get_topic_labels(lang),
    }
